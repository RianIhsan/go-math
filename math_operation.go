package go_math

import "fmt"

func Pertambahan(x, y int) {
	var res int
	res = x + y
	fmt.Println("Masil Dari Pertambahan ", x, " + ", y, " = ", res)
}
func Pengurangan(x, y int) {
	var res int
	res = x - y
	fmt.Println("Masil Dari Pengurangan ", x, " - ", y, " = ", res)
}
func Perkalian(x, y int) {
	var res int
	res = x * y
	fmt.Println("Masil Dari Perkalian ", x, " x ", y, " = ", res)
}
